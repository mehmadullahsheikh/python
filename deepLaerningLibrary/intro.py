### what is a neural network
# a very basic idea
# a neural network is a stack of layers where each layer applies a transformation to the data and passes it to the next layer. it is designed to learn patterns from input features and features and predict outcomes.

### basic architecture
# 1:input layer:takes in features vectors
# 2:hidden layer:apply tranformation using weights,biases and activation fucntions
# 3: output layer:gives  predictions or class scores

####### when use ML vs DL ###
# Use ML when:
# - Data is structured
# - Dataset is small/medium
# - Feature engineering is possible
# - Interpretability is important
# - Limited compute (CPU)

# Use DL when:
# - Data is unstructured (image, text, audio)
# - Dataset is large
# - Feature extraction is complex
# - High accuracy is needed
# - GPU/compute is available